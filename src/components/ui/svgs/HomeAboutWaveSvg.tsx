const SVG_W = 301;
const SVG_H = 319;
const WAVE_MAX = 22; // max px the notch shifts right
const WAVE_MIN = -22; // max px the notch shifts left

export default function HomeAboutWaveSvg({ className }: { className?: string }) {
  const svgRef = useRef<SVGSVGElement>(null);

  useEffect(() => {
    const svg = svgRef.current;
    if (!svg) return;

    const pathEls = Array.from(svg.querySelectorAll('.wave-path')) as SVGPathElement[];
    if (!pathEls.length) return;

    const count = pathEls.length;
    const amplitude = 22;
    const duration = 4; // seconds per full wave cycle
    const waveLength = count; // one full sine wave spans all rows

    // Continuous sine wave — each row gets its own looping tween
    // offset in phase so the wave flows top-to-bottom endlessly.
    const tweens = pathEls.map((el, i) => {
      const phase = (i / waveLength) * Math.PI * 2;
      // Use a proxy to drive a continuous sine
      const proxy = { t: 0 };
      return gsap.to(proxy, {
        t: Math.PI * 2,
        duration,
        repeat: -1,
        ease: 'none',
        onUpdate() {
          const x = Math.sin(proxy.t + phase) * amplitude;
          gsap.set(el, { x });
        },
      });
    });

    return () => { tweens.forEach((tw) => tw.kill()); };
  }, []);

  return (
    <svg
      ref={svgRef}
      className={className}
      width="100%"
      viewBox={`0 0 ${SVG_W} ${SVG_H}`}
      fill="none"
      xmlns="http://www.w3.org/2000/svg"
    >
      <defs>
        {PATHS.map(([, yTop, , fullH], i) => (
          <clipPath key={i} id={`wc-${i}`}>
            <rect x={0} y={yTop} width={SVG_W} height={fullH} />
          </clipPath>
        ))}
      </defs>

      {PATHS.map(([d, yTop, thinH, fullH], i) => (
        <g key={i} clipPath={`url(#wc-${i})`}>
          {/* Static thin line — fills left-edge gap when path moves right */}
          <rect x={0} y={yTop} width={SVG_W} height={thinH} fill="currentColor" />
          {/* Static thick cover at right edge — fills gap when path moves left */}
          <rect x={SVG_W + WAVE_MIN - 2} y={yTop} width={-WAVE_MIN + 2} height={fullH} fill="currentColor" />
          {/* Original curved path — only this animates */}
          <path className="wave-path" d={d} fill="currentColor" />
        </g>
      ))}
    </svg>
  );
}
